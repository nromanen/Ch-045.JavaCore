public class Users {

    private String name;
    private Integer age;
    private String adr;

    public void setName(String name){
        this.name = name;
    }

    public String getName(){
        return name;
    }

    public void setAge(Integer age){
        this.age = age;
    }

    public Integer getAge(){
        return age;
    }

    public void setAdr(String adr){
        this.adr = adr;
    }

    public String getAdr() {
        return adr;
    }

}
